package com.globant.tdd.wordWrapping;

public class WrappedWords {
	private String str;
	private int length;

	public WrappedWords(String str, int length){
		this.str = str;
		this.length = length;
		
	}

	public String getStr() {
		return str;
	}

	public int getLength() {
		return length;
	}
	

}
